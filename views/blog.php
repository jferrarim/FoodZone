					<a href="#"><h4><i class="icon-plus"></i> Ingredients</h4></a>
					<div>
						<ul>
							<li>3 or 4 ripe bananas, smashed</li> 
							<li>1/3 cup melted butter</li>
							<li>1 cup sugar (can easily reduce to 3/4 cup)</li>
							<li>1 egg, beaten</li>
							<li>1 teaspoon vanilla</li>
							<li>1 teaspoon baking soda</li>
							<li>Pinch of salt</li>
							<li>1 1/2 cups of all-purpose flour</li>
						</ul>
					</div>
				</div>	
				<div class="section directions">
					<a href="#"><h4><i class="icon-plus"></i> Directions</h4></a>
					<div>
						<p>No need for a mixer for this recipe. Preheat the oven to 350°F (175°C). With a wooden spoon, mix butter into the mashed bananas in a large mixing bowl. Mix in the sugar, egg, and vanilla. Sprinkle the baking soda and salt over the mixture and mix in. Add the flour last, mix. Pour mixture into a buttered 4x8 inch loaf pan. Bake for 1 hour. Cool on a rack. Remove from pan and slice to serve.</p>
